import React from "react";

const Logo = () => {
  // Esta variável contém a URL da imagem do logotipo.
  const imageUrl = "https://example.com/images/logo.png";

  // Esta variável contém o texto alternativo da imagem do logotipo.
  const altText = "O logotipo do site";

  // Este elemento renderiza a imagem do logotipo.
  return (
    <img src={imageUrl} alt={altText} />
  );
};

export default Logo;

// Este componente renderiza o logotipo do site.

// A variável `imageUrl` contém a URL da imagem do logotipo.

// A variável `altText` contém o texto alternativo da imagem do logotipo.

// O elemento `img` renderiza a imagem do logotipo.

// O atributo `src` do elemento `img` especifica a URL da imagem do logotipo.

// O atributo `alt` do elemento `img` especifica o texto alternativo da imagem do logotipo.


